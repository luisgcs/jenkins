def multiply(a, b):
    return (a*b)

def divide(a, b):
    if (b == 0):
        return False
    return (a/b)

def add(a, b):
    return (a+b)

def subtract(a, b):
    return (a-b)

def odd(a):
    if(a%2 == 0):
        return False
    else:
        return True

def even(a):
    if(a%2 == 0):
        return True
    else:
        return False
